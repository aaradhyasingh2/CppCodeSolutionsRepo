// C++ Program to check whether a number is positive or negative 

#include <iostream> 
using namespace std; 

int main() 
{ 
    int num; 
    num = -100;
    if (num >= 0) { 
        cout << num << " is a positive number." << endl; 
    }
    else { 
        cout << num << " is a negative number." << endl; 
    } 
    return 0; 
}
